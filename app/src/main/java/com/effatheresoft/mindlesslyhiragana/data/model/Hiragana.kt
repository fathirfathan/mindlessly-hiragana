    WO("を"),
    N("ん"),

    // G-line
    GA("が"),
    GI("ぎ"),
    GU("ぐ"),
    GE("げ"),
    GO("ご"),

    // Z-line
    ZA("ざ"),
    JI("じ"),
    ZU("ず"),
    ZE("ぜ"),
    ZO("ぞ"),

    // D-line
    DA("だ"),
    DJI("ぢ"),
