    SO("そ"),

    // T-line
    TA("た"),
    CHI("ち"),
    TSU("つ"),
    TE("て"),
    TO("と"),

    // N-line
    NA("な"),
    NI("に"),
    NU("ぬ"),
    NE("ね"),
