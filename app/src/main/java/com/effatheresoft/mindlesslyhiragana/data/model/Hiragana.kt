    // H-line
    HA("は"),
    HI("ひ"),
    FU("ふ"),
    HE("へ"),
    HO("ほ"),

    // M-line
    MA("ま"),
    MI("み"),
    MU("む"),
    ME("め"),
    MO("も"),

    // Y-line
    YA("や"),
    YU("ゆ"),
    YO("よ"),

    // R-line
    RA("ら"),
    RI("り"),
    RU("る"),
    RE("れ"),
    RO("ろ"),

    // W-line + N
