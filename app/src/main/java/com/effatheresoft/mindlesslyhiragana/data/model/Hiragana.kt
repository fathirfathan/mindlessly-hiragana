    DO("ど"),

    // B-line
    BA("ば"),
    BI("び"),
    BU("ぶ"),
    BE("べ"),
    BO("ぼ"),

    // P-line
    PA("ぱ"),
    PI("ぴ"),
    PU("ぷ"),
    PE("ぺ"),
    PO("ぽ");
}
